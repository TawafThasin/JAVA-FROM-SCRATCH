public class ArithmeitcOperators6
{
    public static void main(String[] args)
    {
        //Arithmetic Works
        int x = 5;
        int y = 3;
        int result1 = x + y;
        int result2 = x - y;
        int result3 = x * y;
        int result4 = x / y;
        int result5 = x % y;
        System.out.println("The Addition is: " + result1);
        System.out.println("The Substraction is: " + result2);
        System.out.println("The Multiplication is: " + result3);
        System.out.println("The Division is: " + result4);
        System.out.println("The Reminder is: " + result5);

        //Increment and Decrement
        int c = 5;
        ++c;
        System.out.println("Pre-Increment Value is: " + c);
        c++;
        System.out.println("Post-Increment Value is: " + c);
        --c;
        System.out.println("Pre-Decrement Value is: " + c);
        c--;
        System.out.println("Post-Decrement Value is: " + c);









    }
}
