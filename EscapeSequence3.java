public class EscapeSequence3
{
    public static void main(String[] args)
    {
        System.out.print("Tawaf Thasin Talukder \nNorth South University \nBSc in CSE \n"); // the \n means new line
        System.out.print("1 \t 2 \n"); // the \t takes 4 spaces
        System.out.print("\"MESSI is the GOAT\""); // the \"abcde\" helps us to put apostrophe inside the line
    }
}
