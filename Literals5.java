public class Literals5
{
    public static void main(String[] args)
    {
        //Binary
        int x = 0b101;
        System.out.println("Binary Value is: " + x);
        //Hexadecimal
        int y = 0x7E;
        System.out.println("Hexadecimal Value is: "+ y);
        //Octal
        int z = 012;
        System.out.println("Octal Value is: "+ z);

    }
}
