public class RelationalOperators7
{
    public static void main(String[] args)
    {
        int x = 6;
        int y = 7;
        boolean result1 = x > y;
        System.out.println(result1);
        boolean result2 = x < y;
        System.out.println(result2);
        boolean result3 = x == y;
        System.out.println(result3);
        boolean result4 = x >= y;
        System.out.println(result4);
        boolean result5 = x <= y;
        System.out.println(result5);
    }
}
